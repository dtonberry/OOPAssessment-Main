    public StaffAllOrders(Staff staff) {
        loggedInStaff = staff;
        initComponents();
        this.getContentPane().setBackground(Color.lightGray); //set colour to light grey

        DBManager db = new DBManager();
        //this will load both customers and orders simultaniously
        HashMap<String, Customer> customers = db.loadCustomers();

        DefaultTableModel orderTable = (DefaultTableModel) tblOrders.getModel();
        
        for (Map.Entry<String, Customer> customerEntry : customers.entrySet()) {
            Customer customer = customerEntry.getValue();

            //this will loop through all of the orders the customer has made
            //and places them in to the table
            for (Map.Entry<Integer, Order> orderEntry : customer.getOrders().entrySet()) {
                Order order = orderEntry.getValue();

                orderTable.addRow(new Object[]{
                    customer.getUsername(),
                    order.getOrderId(),
                    new SimpleDateFormat("dd/MM/yyyy HH:mm:ss").format(order.getOrderDate()),
                    "Â£" + order.getOrderTotal()
                });
            }
        }
    }

